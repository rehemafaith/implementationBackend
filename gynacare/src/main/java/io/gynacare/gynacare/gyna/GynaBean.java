package io.gynacare.gynacare.gyna;

import java.math.BigDecimal;

public class GynaBean {
    private BigDecimal gynaId;
    private String gynaFullName;
    private BigDecimal gynaRegNo;
    private String gynaLocation;
    private String gynaEmail;
    private String gynaMobile;
    private BigDecimal gynaLoginId;
    public BigDecimal getGynaId() {
        return gynaId;
    }
    public void setGynaId(BigDecimal gynaId) {
        this.gynaId = gynaId;
    }
    public String getGynaFullName() {
        return gynaFullName;
    }
    public void setGynaFullName(String gynaFullName) {
        this.gynaFullName = gynaFullName;
    }
    public BigDecimal getGynaRegNo() {
        return gynaRegNo;
    }
    public void setGynaRegNo(BigDecimal gynaRegNo) {
        this.gynaRegNo = gynaRegNo;
    }
    public String getGynaLocation() {
        return gynaLocation;
    }
    public void setGynaLocation(String gynaLocation) {
        this.gynaLocation = gynaLocation;
    }
    public String getGynaEmail() {
        return gynaEmail;
    }
    public void setGynaEmail(String gynaEmail) {
        this.gynaEmail = gynaEmail;
    }
    public String getGynaMobile() {
        return gynaMobile;
    }
    public void setGynaMobile(String gynaMobile) {
        this.gynaMobile = gynaMobile;
    }
    public BigDecimal getGynaLoginId() {
        return gynaLoginId;
    }
    public void setGynaLoginId(BigDecimal gynaLoginId) {
        this.gynaLoginId = gynaLoginId;
    }

    
}

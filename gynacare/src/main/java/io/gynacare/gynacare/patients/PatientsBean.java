package io.gynacare.gynacare.patients;

import java.math.BigDecimal;

public class PatientsBean {
    private BigDecimal patientId;
    private String patientFullName;
    private String patientEmail;
    private BigDecimal patientLoginId;
    private BigDecimal patientWeight;
    private BigDecimal patientAge;
    private String patientGender;
    private String patientMobile;
    public BigDecimal getPatientId() {
        return patientId;
    }
    public void setPatientId(BigDecimal patientId) {
        this.patientId = patientId;
    }
    public String getPatientFullName() {
        return patientFullName;
    }
    public void setPatientFullName(String patientFullName) {
        this.patientFullName = patientFullName;
    }
    public String getPatientEmail() {
        return patientEmail;
    }
    public void setPatientEmail(String patientEmail) {
        this.patientEmail = patientEmail;
    }
    public BigDecimal getPatientLoginId() {
        return patientLoginId;
    }
    public void setPatientLoginId(BigDecimal patientLoginId) {
        this.patientLoginId = patientLoginId;
    }
    public BigDecimal getPatientWeight() {
        return patientWeight;
    }
    public void setPatientWeight(BigDecimal patientWeight) {
        this.patientWeight = patientWeight;
    }
    public BigDecimal getPatientAge() {
        return patientAge;
    }
    public void setPatientAge(BigDecimal patientAge) {
        this.patientAge = patientAge;
    }
    public String getPatientGender() {
        return patientGender;
    }
    public void setPatientGender(String patientGender) {
        this.patientGender = patientGender;
    }
    public String getPatientMobile() {
        return patientMobile;
    }
    public void setPatientMobile(String patientMobile) {
        this.patientMobile = patientMobile;
    }



}
